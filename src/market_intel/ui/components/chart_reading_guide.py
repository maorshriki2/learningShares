def render_chart_reading_guide(guide_key: str, *, expander_title: str | None = None) -> None:
    """Show a collapsed expander with Hebrew tips (charts, fundamentals, peers — by guide_key)."""
    body = _CHART_GUIDES.get(guide_key)
    if not body:
        return
    title = expander_title or "📖 מה להסתכל בגרף? איך לקרוא את מה שמוצג"
    with st.expander(title, expanded=False):
        st.markdown(body.strip())
