def render_chart_reading_guide(guide_key: str, *, expander_title: str | None = None) -> None:
    """Show a collapsed expander with Hebrew tips (charts, fundamentals, peers — by guide_key)."""
    pack = _load_knowledge_pack()
    if guide_key in pack:
        body: Any = {
            "beginner": pack[guide_key].get("beginner", ""),
            "advanced": pack[guide_key].get("advanced", ""),
        }
    else:
        body = _CHART_GUIDES.get(guide_key)
    if not body:
        return
    title = expander_title or "📖 מה להסתכל בגרף? איך לקרוא את מה שמוצג"
    with st.expander(title, expanded=False):
        if isinstance(body, dict) and ("beginner" in body or "advanced" in body):
            beginner = str(body.get("beginner") or "").strip()
            advanced = str(body.get("advanced") or "").strip()
            if beginner:
                render_focus_heading("Beginner — קריאה ממוקדת", variant="steps")
                render_focus_block(beginner, variant="steps")
            if advanced:
                with st.expander("Advanced (פירוט מקצועי)", expanded=False):
                    render_focus_block(advanced, variant="neutral")
        else:
            render_focus_block(str(body).strip(), variant="neutral")
