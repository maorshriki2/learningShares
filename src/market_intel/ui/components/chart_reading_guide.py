def _beginner_advanced(*, beginner_md: str, advanced_md: str) -> dict[str, str]:
    """
    Content container for UX:
    - Beginner: short, action-first checklist
    - Advanced: deeper, drier, more technical
    """
    return {"beginner": beginner_md.strip(), "advanced": advanced_md.strip()}


@st.cache_data(ttl=60 * 60, show_spinner=False)
def _load_knowledge_pack() -> dict[str, dict[str, str]]:
    """
    Load external knowledge pack (optional).
    Returns: {key: {"beginner": md, "advanced": md}}
    """
    p = Path(__file__).resolve().parents[1] / "content" / "knowledge_pack.json"
    if not p.exists():
        return {}
    try:
        data = json.loads(p.read_text(encoding="utf-8"))
    except Exception:
        return {}
    if not isinstance(data, dict):
        return {}
    items = data.get("items")
    if not isinstance(items, list):
        return {}
    out: dict[str, dict[str, str]] = {}
    for it in items:
        if not isinstance(it, dict):
            continue
        key = str(it.get("key") or "").strip()
        aud = str(it.get("audience") or "").strip().lower()
        if not key or aud not in {"beginner", "advanced"}:
            continue
        title = str(it.get("title") or "").strip()
        rule = str(it.get("rule") or "").strip()
        why = str(it.get("why") or "").strip()
        how = it.get("how") if isinstance(it.get("how"), list) else []
        mistakes = it.get("mistakes") if isinstance(it.get("mistakes"), list) else []
        example = str(it.get("example") or "").strip()

        md_parts: list[str] = []
        if title:
            md_parts.append(f"**{title}**")
        if rule:
            md_parts.append(f"**Rule:** {rule}")
        if why:
            md_parts.append(f"**Why:** {why}")
        if how:
            md_parts.append("**How:**\n" + "\n".join(f"- {str(x)}" for x in how if str(x).strip()))
        if mistakes:
            md_parts.append(
                "**Common mistakes:**\n" + "\n".join(f"- {str(x)}" for x in mistakes if str(x).strip())
            )
        if example:
            md_parts.append(f"**Example:** {example}")

        out.setdefault(key, {})
        out[key][aud] = "\n\n".join([p for p in md_parts if p])
    return out


